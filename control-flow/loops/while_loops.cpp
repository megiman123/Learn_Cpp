#include <iostream>
using namespace std;
int main() {
  /*
  body of while loop- 
  while (condition) {
    // code to be executed
  }
  */
    //initialization
    int i = 0; 
    // while loop that starts with i = 1 and ends
  	// when i is greater than 5.
    while(i <= 5){
        cout << i <<" ";

        i++; // increment
    }

    return 0;
}